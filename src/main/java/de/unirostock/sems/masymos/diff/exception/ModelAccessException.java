package de.unirostock.sems.masymos.diff.exception;

public class ModelAccessException extends DiffException {

	private static final long serialVersionUID = -4484115590245728922L;

	public ModelAccessException() {
		super();
	}

	public ModelAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModelAccessException(String message) {
		super(message);
	}

	public ModelAccessException(Throwable cause) {
		super(cause);
	}

	
}
